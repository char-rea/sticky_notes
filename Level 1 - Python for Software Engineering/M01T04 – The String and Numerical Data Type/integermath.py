# ask the user for three different integers and perform different mathematical operations on them and print the results

num1 = int(input("Please enter the first number: "))
num2 = int(input("Please enter the second number: "))
num3 = int(input("Please enter the third number: "))

sum1 = (num1 + num2 + num3)
print(sum1)

sum2 = (num1 - num2)
print(sum2)

sum3 = (num3 * num1)
print(sum3)

sum4 = (num1 + num2 + num3) / num3
print(sum4)